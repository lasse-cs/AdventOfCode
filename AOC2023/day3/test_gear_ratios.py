from gear_ratios import Grid


def test_get_part_sum():
    text = """467..114..
    ...*......
    ..35..633.
    ......#...
    617*......
    .....+.58.
    ..592.....
    ......755.
    ...$.*....
    .664.598.."""
    grid = Grid.parse(text)
    assert grid.get_part_number_sum() == 4361


